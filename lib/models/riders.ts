
export class Riders {
    name: String;
    nation: String;
    num: Number;
    image: String;
    flag_image: String;
    profileLink: String;

    constructor(name: String, nation: String, num: Number, image: String, flag_image: String, profileLink: String){
        this.name = name;
        this.nation = nation;
        this.num = num;
        this.image = image;
        this.flag_image = flag_image;
        this.profileLink = profileLink;
    }
}